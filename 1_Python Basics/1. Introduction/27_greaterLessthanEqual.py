# This are mathematical operations for checking conditions and making decisions

greater = 10
less = 5

print("\n==========================================================\n")
print('\tMathematical Operations in decision making!\n')

print('Greater than:')
if greater > less: print('Yes, it is greater')  #try different values
else: print('Oooh No!')

print('\nLess than:')
if greater < less: print('Yes, it is greater')  #try different values
else: print('Oooh No!')

print('\nEqual to:')
if greater == less: print('Yes, they are equal')  #try different values
else: print('Ouch! ' + str(greater) + ' can\'t be equal to ' + str(less))

# End of the program
print("\n==========================================================\n")

