pets = ["lion", "dragon", "horse", "bear"]
print(pets)

#getting a specific element in the list (array)
print(pets[0].title())
print(pets[1].title())
print(pets[2].title())
print(pets[3].title())

# Reversed printing
print("\nLet's us start printing from the end of the list\n")
print(pets[-1].title())
print(pets[-2].title())
print(pets[-3].title())
print(pets[-4].title())
